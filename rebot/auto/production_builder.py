def _render_web_workbench(product: str, ui: UIDesignSchema | None) -> str:
    pages = ui.pages if ui else ["Home", "Explore", "Insights", "Profile"]
    task_items = "\n".join(
        f"          <li><span className=\"dot\"></span>{page} flow</li>" for page in pages[:6]
    )
    return (
        "import './styles.css'\n"
        "import { useMemo, useState } from 'react'\n"
        "import Editor from '@monaco-editor/react'\n\n"
        "export default function App() {\n"
        "  const [prompt, setPrompt] = useState('Build a fitness iOS app with a streak system.')\n"
        "  const [events, setEvents] = useState([])\n"
        "  const [toolCalls, setToolCalls] = useState([])\n"
        "  const [runId, setRunId] = useState(null)\n"
        "  const [tasks, setTasks] = useState(['Spec compile', 'Architecture', 'UI layout', 'Codegen'])\n"
        "  const [workspace, setWorkspace] = useState('C:/workspace/project')\n"
        "  const [files, setFiles] = useState([])\n"
        "  const [activeFile, setActiveFile] = useState({ path: '', language: 'plaintext', content: '' })\n"
        "  const [originalContent, setOriginalContent] = useState('')\n"
        "  const [search, setSearch] = useState('')\n"
        "  const [showDiff, setShowDiff] = useState(false)\n"
        "  const [status, setStatus] = useState('idle')\n"
        "  const [previewUrl, setPreviewUrl] = useState('http://localhost:5173')\n"
        "  const [previewInput, setPreviewInput] = useState('http://localhost:5173')\n"
        "  const [securityRun, setSecurityRun] = useState(null)\n"
        "  const wsUrl = useMemo(() => {\n"
        "    const host = window.location.host\n"
        "    const proto = window.location.protocol === 'https:' ? 'wss' : 'ws'\n"
        "    return `${proto}://${host}/ws/events`\n"
        "  }, [])\n\n"
        "  function handleExecute() {\n"
        "    setStatus('running')\n"
        "    fetch('/api/execute', {\n"
        "      method: 'POST',\n"
        "      headers: { 'Content-Type': 'application/json' },\n"
        "      body: JSON.stringify({\n"
        "        task: prompt,\n"
        "        api_key: 'YOUR_API_KEY',\n"
        "        model: 'gpt-4o-mini',\n"
        "        base_url: 'https://api.openai.com/v1',\n"
        "        workspace\n"
        "      })\n"
        "    })\n"
        "      .then(res => res.json())\n"
        "      .then(data => {\n"
        "        setRunId(data.run_id)\n"
        "        subscribeEvents(data.run_id)\n"
        "      })\n"
        "  }\n\n"
        "  function subscribeEvents(targetRunId) {\n"
        "    const socket = new WebSocket(wsUrl)\n"
        "    socket.onmessage = (event) => {\n"
        "      const batch = JSON.parse(event.data)\n"
        "      if (batch.run_id && targetRunId && batch.run_id !== targetRunId) return\n"
        "      const list = batch.events || []\n"
        "      setEvents(prev => [...list.reverse(), ...prev].slice(0, 200))\n"
        "      list.forEach((payload) => {\n"
        "        if (payload.type === 'tool_call') {\n"
        "          setToolCalls(prev => [payload.payload, ...prev].slice(0, 50))\n"
        "        }\n"
        "        if (payload.type === 'tool_start') {\n"
        "          setToolCalls(prev => [{ name: payload.name, args: payload.payload }, ...prev].slice(0, 50))\n"
        "        }\n"
        "        if (payload.type === 'tool_end') {\n"
        "          setToolCalls(prev => [{ name: payload.name, args: payload.payload }, ...prev].slice(0, 50))\n"
        "        }\n"
        "      })\n"
        "    }\n"
        "    socket.onclose = () => setStatus('idle')\n"
        "  }\n\n"
        "  return (\n"
        "    <div className=\"workbench\">\n"
        "      <header className=\"wb-top\">\n"
        f"        <div className=\"brand\">{product} Agent Studio</div>\n"
        "        <div className={`status ${status}`}>{status}</div>\n"
        "      </header>\n"
        "      <section className=\"wb-body\">\n"
        "        <aside className=\"wb-left\">\n"
        "          <div className=\"panel-title\">AI Tasks</div>\n"
        "          <div className=\"task-tree\">\n"
        "            <ul>\n"
        f"{task_items}\n"
        "            </ul>\n"
        "          </div>\n"
        "          <div className=\"panel-title\">Tools</div>\n"
        "          <div className=\"tool-list\">\n"
        "            {toolCalls.length === 0 ? <div className=\"empty\">No tool calls yet.</div> : null}\n"
        "            {toolCalls.map((t, idx) => (\n"
        "              <div key={idx} className=\"tool-call\">\n"
        "                <div className=\"tool-name\">{t.name || 'tool'}</div>\n"
        "                <div className=\"tool-args\">{JSON.stringify(t.args || {})}</div>\n"
        "              </div>\n"
        "            ))}\n"
        "          </div>\n"
        "        </aside>\n"
        "        <main className=\"wb-center\">\n"
        "          <div className=\"panel-title\">Prompt</div>\n"
        "          <textarea value={prompt} onChange={e => setPrompt(e.target.value)} />\n"
        "          <div className=\"actions\">\n"
        "            <button className=\"cta\" onClick={handleExecute}>Run Agent</button>\n"
        "            <button className=\"ghost\" onClick={loadWorkspace}>Load Files</button>\n"
        "          </div>\n"
        "          <div className=\"panel-title\">Workspace</div>\n"
        "          <div className=\"workspace-input\">\n"
        "            <input value={workspace} onChange={e => setWorkspace(e.target.value)} placeholder=\"Workspace path\" />\n"
        "            <button className=\"ghost\" onClick={chooseWorkspace}>Choose</button>\n"
        "            <button className=\"ghost\" onClick={saveFile}>Save File</button>\n"
        "            <button className=\"ghost\" onClick={formatContent}>Format</button>\n"
        "            <button className=\"ghost\" onClick={() => setShowDiff(!showDiff)}>Diff</button>\n"
        "          </div>\n"
        "          <div className=\"panel-title\">Workspace</div>\n"
        "          <div className=\"editor-shell\">\n"
        "            <div className=\"file-tree\">\n"
        "              <input className=\"file-search\" placeholder=\"Search files\" value={search} onChange={e => setSearch(e.target.value)} />\n"
        "              {files.map((f, idx) => (\n"
        "                !search || f.toLowerCase().includes(search.toLowerCase()) ? (\n"
        "                <button\n"
        "                  key={idx}\n"
        "                  className={`file-item ${activeFile.path === f ? 'active' : ''}`}\n"
        "                  onClick={() => openFile(f)}\n"
        "                >\n"
        "                  {f}\n"
        "                </button>\n"
        "                ) : null\n"
        "              ))}\n"
        "            </div>\n"
        "            <div className=\"editor-pane\">\n"
        "              <div className=\"editor-header\">{activeFile.path}</div>\n"
        "              {showDiff ? (\n"
        "                <pre className=\"diff-view\">{diffLines()}</pre>\n"
        "              ) : (\n"
        "                <Editor\n"
        "                  height=\"360px\"\n"
        "                  language={activeFile.language}\n"
        "                  value={activeFile.content}\n"
        "                  onChange={(value) => setActiveFile(prev => ({ ...prev, content: value || '' }))}\n"
        "                  theme=\"vs-dark\"\n"
        "                  options={{ minimap: { enabled: false }, fontSize: 13 }}\n"
        "                />\n"
        "              )}\n"
        "            </div>\n"
        "          </div>\n"
        "        </main>\n"
        "        <aside className=\"wb-right\">\n"
        "          <div className=\"panel-title\">Preview</div>\n"
        "          <div className=\"preview-controls\">\n"
        "            <button className=\"ghost\" onClick={() => { setPreviewUrl('http://localhost:5173'); setPreviewInput('http://localhost:5173'); }}>Web</button>\n"
        "            <button className=\"ghost\" onClick={() => { setPreviewUrl('http://localhost:19006'); setPreviewInput('http://localhost:19006'); }}>Expo Web</button>\n"
        "            <input className=\"preview-input\" value={previewInput} onChange={e => setPreviewInput(e.target.value)} placeholder=\"Preview URL\" />\n"
        "            <button className=\"ghost\" onClick={() => setPreviewUrl(previewInput)}>Go</button>\n"
        "            <button className=\"ghost\" onClick={runSecurityTest}>Run Security Test</button>\n"
        "          </div>\n"
        "          <div className=\"preview\">\n"
        "            <iframe title=\"preview\" src={previewUrl} />\n"
        "          </div>\n"
        "          <div className=\"panel-title\">WebSocket Events</div>\n"
        "          <div className=\"event-stream\">\n"
        "            {events.length === 0 ? <div className=\"empty\">No events yet.</div> : null}\n"
        "            {events.map((ev, idx) => (\n"
        "              <div key={idx} className=\"event\">\n"
        "                <div className=\"event-type\">{ev.type}</div>\n"
        "                <pre>{JSON.stringify(ev.payload || ev, null, 2)}</pre>\n"
        "              </div>\n"
        "            ))}\n"
        "          </div>\n"
        "        </aside>\n"
        "      </section>\n"
        "    </div>\n"
        "  )\n"
        "}\n"
    )


