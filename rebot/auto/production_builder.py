"""Production scaffold builder."""

from __future__ import annotations

from dataclasses import dataclass
from pathlib import Path
from typing import Any

from rebot.auto.spec_compiler import SpecSchema
from rebot.auto.arch_synthesizer import ArchitectureSchema
from rebot.auto.ui_designer import UIDesignSchema
from rebot.auto.desktop_scaffold import build_electron_app


@dataclass
class ProductionBuilder:
    root: Path

    def build(
        self,
        spec: SpecSchema,
        arch: ArchitectureSchema,
        *,
        language: str,
        ui: UIDesignSchema | None = None,
        scaffold: bool = True,
    ) -> None:
        self.root.mkdir(parents=True, exist_ok=True)
        (self.root / "docs").mkdir(exist_ok=True)
        (self.root / "backend").mkdir(exist_ok=True)
        (self.root / "frontend").mkdir(exist_ok=True)
        (self.root / "mobile").mkdir(exist_ok=True)
        (self.root / "desktop").mkdir(exist_ok=True)
        (self.root / "miniapp").mkdir(exist_ok=True)
        (self.root / "infra").mkdir(exist_ok=True)

        (self.root / "docs" / "SPEC.json").write_text(spec.model_dump_json(), encoding="utf-8")
        (self.root / "docs" / "ARCH.json").write_text(arch.model_dump_json(), encoding="utf-8")
        if ui is not None:
            (self.root / "docs" / "UI.json").write_text(ui.model_dump_json(), encoding="utf-8")
        (self.root / "README.md").write_text(
            f"# {spec.product}\n\nGenerated by Rebot.\n", encoding="utf-8"
        )

        if scaffold:
            if language.lower() == "python":
                (self.root / "backend" / "app.py").write_text(
                    "from fastapi import FastAPI\n\napp = FastAPI()\n\n@app.get('/health')\ndef health():\n    return {'ok': True}\n",
                    encoding="utf-8",
                )
                (self.root / "backend" / "requirements.txt").write_text(
                    "fastapi\nuvicorn\n", encoding="utf-8"
                )
            elif language.lower() in {"ts", "typescript", "node"}:
                (self.root / "backend" / "index.ts").write_text(
                    "import express from 'express';\nconst app = express();\n", encoding="utf-8"
                )

            (self.root / "frontend" / "README.md").write_text(
                _render_frontend_readme(spec.product), encoding="utf-8"
            )
            (self.root / "frontend" / "src").mkdir(parents=True, exist_ok=True)
            (self.root / "frontend" / "src" / "main.jsx").write_text(
                _render_web_main(),
                encoding="utf-8",
            )
            (self.root / "frontend" / "src" / "App.jsx").write_text(
                _render_web_workbench(spec.product, ui),
                encoding="utf-8",
            )
            (self.root / "frontend" / "src" / "styles.css").write_text(
                _render_web_styles(ui) + "\n" + _render_workbench_styles(),
                encoding="utf-8",
            )
            (self.root / "frontend" / "index.html").write_text(
                _render_web_index(spec.product),
                encoding="utf-8",
            )
            (self.root / "frontend" / "package.json").write_text(
                _render_web_package_json(spec.product),
                encoding="utf-8",
            )
            (self.root / "frontend" / "vite.config.js").write_text(
                _render_vite_config(),
                encoding="utf-8",
            )
            build_electron_app(self.root / "desktop" / "electron", spec.product)
            _build_ios_app(self.root / "mobile" / "ios", spec.product, ui)
            _build_expo_app(self.root / "mobile" / "expo", spec.product, ui)
            (self.root / "miniapp" / "README.md").write_text(
                "Mini-program scaffold placeholder.\n", encoding="utf-8"
            )
            _build_wechat_miniapp(self.root / "miniapp" / "wechat", spec.product, ui)
        if scaffold:
            (self.root / "infra" / "Dockerfile").write_text(
                "FROM python:3.11-slim\nWORKDIR /app\nCOPY backend /app/backend\n",
                encoding="utf-8",
            )
            (self.root / "infra" / "docker-compose.yml").write_text(
                "version: '3'\nservices:\n  backend:\n    build: .\n    ports: ['8000:8000']\n",
                encoding="utf-8",
            )


def _render_swiftui_theme(ui: UIDesignSchema | None) -> str:
    if ui is None:
        return "import SwiftUI\n\nstruct Theme { static let primary = Color.blue }\n"
    primary = ui.colors.get("primary", "#16A34A")
    bg = ui.colors.get("background", "#F6F7FB")
    accent = ui.colors.get("accent", "#0EA5E9")
    return (
        "import SwiftUI\n\n"
        "struct Theme {\n"
        f"  static let primary = Color(\"{primary}\")\n"
        f"  static let background = Color(\"{bg}\")\n"
        f"  static let accent = Color(\"{accent}\")\n"
        "}\n"
    )


def _render_home_view(product: str, ui: UIDesignSchema | None) -> str:
    title = product or "Smart App"
    spacing = 16
    if ui and ui.spacing.get("md"):
        spacing = int(ui.spacing.get("md", 16))
    return (
        "import SwiftUI\n\n"
        "struct HomeView: View {\n"
        "  var body: some View {\n"
        f"    VStack(spacing: {spacing}) {{\n"
        f"      Text(\"{title}\").font(.largeTitle).bold()\n"
        "      Text(\"Today\\'s focus\").font(.headline)\n"
        "      RoundedRectangle(cornerRadius: 18).fill(Theme.primary.opacity(0.15)).frame(height: 140)\n"
        "      RoundedRectangle(cornerRadius: 18).fill(Theme.accent.opacity(0.12)).frame(height: 100)\n"
        "      Button(action: {}) { Text(\"Get Started\").padding().background(Theme.primary).foregroundColor(.white).cornerRadius(12) }\n"
        "    }.padding(24).background(Theme.background)\n"
        "  }\n"
        "}\n"
    )


def _build_ios_app(path: Path, product: str, ui: UIDesignSchema | None) -> None:
    path.mkdir(parents=True, exist_ok=True)
    (path / "README.md").write_text(
        "SwiftUI scaffold. Open in Xcode and create a new iOS App, then replace files.\n",
        encoding="utf-8",
    )
    (path / "Theme.swift").write_text(_render_swiftui_theme(ui), encoding="utf-8")
    (path / "HomeView.swift").write_text(
        _render_home_view(product, ui),
        encoding="utf-8",
    )
    (path / "AppMain.swift").write_text(
        "import SwiftUI\n\n@main\nstruct AppMain: App { var body: some Scene { WindowGroup { HomeView() } } }\n",
        encoding="utf-8",
    )


def _build_expo_app(path: Path, product: str, ui: UIDesignSchema | None) -> None:
    path.mkdir(parents=True, exist_ok=True)
    (path / "package.json").write_text(_render_expo_package_json(product), encoding="utf-8")
    (path / "app.json").write_text(_render_expo_app_json(product), encoding="utf-8")
    (path / "App.tsx").write_text(_render_expo_app(product, ui), encoding="utf-8")
    (path / "tsconfig.json").write_text(
        "{\n  \"extends\": \"expo/tsconfig.base\"\n}\n", encoding="utf-8"
    )
    (path / "babel.config.js").write_text(
        "module.exports = function(api) { api.cache(true); return { presets: ['babel-preset-expo'] }; };\n",
        encoding="utf-8",
    )


