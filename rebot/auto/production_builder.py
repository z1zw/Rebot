def _build_wechat_miniapp(path: Path, product: str, ui: UIDesignSchema | None) -> None:
    path.mkdir(parents=True, exist_ok=True)
    (path / "app.js").write_text("App({})\n", encoding="utf-8")
    (path / "app.json").write_text(
        "{\n  \"pages\": [\"pages/index/index\"],\n  \"window\": {\"navigationBarTitleText\": \""
        + product
        + "\"}\n}\n",
        encoding="utf-8",
    )
    (path / "app.wxss").write_text("page { background: #f7f7fb; }\n", encoding="utf-8")
    pages = path / "pages" / "index"
    pages.mkdir(parents=True, exist_ok=True)
    (pages / "index.wxml").write_text(
        "<view class=\"page\">\n  <view class=\"title\">" + product + "</view>\n  <view class=\"card\"></view>\n  <button class=\"cta\">Get started</button>\n</view>\n",
        encoding="utf-8",
    )
    (pages / "index.js").write_text("Page({})\n", encoding="utf-8")
    (pages / "index.json").write_text("{\"usingComponents\":{}}\n", encoding="utf-8")
    (pages / "index.wxss").write_text(
        ".page { padding: 24rpx; }\n.title { font-size: 40rpx; font-weight: 600; margin-bottom: 24rpx; }\n.card { height: 240rpx; border-radius: 24rpx; background: rgba(14,165,233,0.12); margin-bottom: 24rpx; }\n.cta { background: #0f172a; color: #fff; border-radius: 999rpx; }\n",
        encoding="utf-8",
    )


